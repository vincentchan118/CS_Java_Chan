public class UsingVariables {
	
	public static void main(String[] args)
	{
		int room;
		double a;
		String b;
		
		room = 113;
		a = 2.71828;
		b = "Computer Science";
		
		System.out.println("This is room " + room);
		System.out.println("e is close to " + a);
		System.out.println("I am learning about " + b);
	}

}